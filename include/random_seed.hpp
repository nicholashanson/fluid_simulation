#ifndef RANDOM_SEED_HPP
#define RANDOM_SEED_HPP

namespace fs {
namespace lga {

    template<typename Array, typename MDSpan>
    void random_seed( grid<Array, MDSpan>& gd ) {

        std::random_device rd;
        std::mt19937 gen( rd() );
        std::discrete_distribution<int> dist( { 2, 96, 2 } );

        for ( size_t i = 1; i < gd.get_grid_height() - 1; ++i ) {
            for ( size_t j = 1; j < gd.get_grid_width() - 1; ++j ) {

                auto v_x = dist( gen );
                auto v_y = dist( gen );

                gd.set_cell_state( { v_x - 1, v_y - 1 }, i, j );

            }
        }

    }

}
}

#endif



































































































































































































































